// ================================================================
// Draw a single glyph at character cell (col, row)
// ================================================================

static void draw_glyph(uint32_t col, uint32_t row, char c,
                       uint32_t fg, uint32_t bg)
{
    uint32_t px = col * FONT_WIDTH;
    uint32_t py = row * FONT_HEIGHT;

    uint8_t glyphIdx = 0;
    if (c >= 0x20 && c <= 0x7F) {
        glyphIdx = static_cast<uint8_t>(c - 0x20);
    }
    // else use space (index 0)

    const uint8_t* glyph = s_font[glyphIdx];

    for (uint8_t y = 0; y < FONT_HEIGHT; ++y) {
        uint8_t bits = glyph[y];
        for (uint8_t x = 0; x < FONT_WIDTH; ++x) {
            uint32_t color = (bits & (0x80 >> x)) ? fg : bg;
            framebuffer::put_pixel(px + x, py + y, color);
        }
    }
}

// ================================================================
// Scroll the framebuffer up by one text row
// ================================================================

static void scroll()
{
    if (!framebuffer::is_available()) return;

    uint32_t* buf = framebuffer::get_buffer();
    if (!buf) return;

    uint32_t pitch32 = framebuffer::get_pitch() / 4;
    uint32_t rowPixels = FONT_HEIGHT * pitch32;
    uint32_t totalRows = s_rows;

    // Move all rows up by one
    for (uint32_t y = 0; y < (totalRows - 1) * FONT_HEIGHT; ++y) {
        uint32_t srcY = y + FONT_HEIGHT;
        for (uint32_t x = 0; x < s_cols * FONT_WIDTH; ++x) {
            buf[y * pitch32 + x] = buf[srcY * pitch32 + x];
        }
    }

    // Clear the last row
    uint32_t lastRowStart = (totalRows - 1) * FONT_HEIGHT;
    for (uint32_t y = 0; y < FONT_HEIGHT; ++y) {
        for (uint32_t x = 0; x < s_cols * FONT_WIDTH; ++x) {
            buf[(lastRowStart + y) * pitch32 + x] = s_bg;
        }
    }

    (void)rowPixels;
}

// ================================================================
// Public API
// ================================================================

bool init()
{
    if (!framebuffer::is_available()) return false;

    s_cols = framebuffer::get_width()  / FONT_WIDTH;
    s_rows = framebuffer::get_height() / FONT_HEIGHT;
    s_row  = 0;
    s_col  = 0;
    s_fg   = COLOR_LIGHT_GRAY;
    s_bg   = COLOR_BLACK;
    s_inited = true;

    return true;
}

void clear()
{
    if (!s_inited) return;
    framebuffer::clear(s_bg);
    s_row = 0;
    s_col = 0;
}

void set_color(uint32_t fg, uint32_t bg)
{
    s_fg = fg;
    s_bg = bg;
}

void putchar(char c)
{
    if (!s_inited) return;

    if (c == '\n') {
        s_col = 0;
        ++s_row;
        if (s_row >= s_rows) {
            scroll();
            s_row = s_rows - 1;
        }
        return;
    }

    if (c == '\r') {
        s_col = 0;
        return;
    }

    if (c == '\t') {
        s_col = (s_col + 4) & ~3u;
        if (s_col >= s_cols) {
            s_col = 0;
            ++s_row;
            if (s_row >= s_rows) {
                scroll();
                s_row = s_rows - 1;
            }
        }
        return;
    }

    draw_glyph(s_col, s_row, c, s_fg, s_bg);

    ++s_col;
    if (s_col >= s_cols) {
        s_col = 0;
        ++s_row;
        if (s_row >= s_rows) {
            scroll();
            s_row = s_rows - 1;
        }
    }
}

void print(const char* str)
{
    if (!str) return;
    while (*str) {
        putchar(*str++);
    }
}

void print(const char* str, uint32_t length)
{
    if (!str) return;
    for (uint32_t i = 0; i < length; ++i) {
        putchar(str[i]);
    }
}

void print_dec(int32_t value)
{
    if (value == 0) { putchar('0'); return; }
    if (value < 0) { putchar('-'); value = -value; }

    char buf[12];
    int pos = 0;
    while (value > 0) {
        buf[pos++] = static_cast<char>('0' + (value % 10));
        value /= 10;
    }
    while (pos > 0) putchar(buf[--pos]);
}

void print_hex(uint32_t value)
{
    static const char hex[] = "0123456789ABCDEF";
    print("0x");
    bool leading = true;
    for (int i = 7; i >= 0; --i) {
        uint8_t d = (value >> (i * 4)) & 0xF;
        if (d != 0) leading = false;
        if (!leading || i == 0) putchar(hex[d]);
    }
}

void print_hex64(uint64_t value)
{
    static const char hex[] = "0123456789ABCDEF";
    print("0x");
    bool leading = true;
    for (int i = 15; i >= 0; --i) {
        uint8_t d = static_cast<uint8_t>((value >> (i * 4)) & 0xF);
        if (d != 0) leading = false;
        if (!leading || i == 0) putchar(hex[d]);
    }
}

void print_colored(const char* str, uint32_t fg, uint32_t bg)
{
    uint32_t oldFg = s_fg;
    uint32_t oldBg = s_bg;
    s_fg = fg;
    s_bg = bg;
    print(str);
    s_fg = oldFg;
    s_bg = oldBg;
}

void get_cursor(uint32_t& row, uint32_t& col)
{
    row = s_row;
    col = s_col;
}

void set_cursor(uint32_t row, uint32_t col)
{
    if (row >= s_rows) row = s_rows - 1;
    if (col >= s_cols) col = s_cols - 1;
    s_row = row;
    s_col = col;
}

uint32_t get_cols() { return s_cols; }
uint32_t get_rows() { return s_rows; }

